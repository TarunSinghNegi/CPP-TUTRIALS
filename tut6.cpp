#include<iostream>
 using namespace std;

 int main()
 {
     int a = 4, b =8;
     cout<<"showing some arithmatic operators"<<endl ;
     cout<<"THE VALUE OF a + b is "<<a + b<<endl;
     cout<<"THE VALUE OF a - b is "<<a - b<<endl;
     cout<<"THE VALUE OF a * b is "<<a * b<<endl;
     cout<<"THE VALUE OF a / b is "<<a / b<<endl;
     cout<<"THE VALUE OF a++ is "<<a++<<endl;
     cout<<"THE VALUE OF a-- is "<<a--<<endl;
     cout<<"THE VALUE OF ++a  is "<<++a <<endl;
     return 0;
 }

